#include <iostream>
using namespace std;

int main(int argc, const char* argv[])
{
    long long number1, number2;

    cin >> number1 >> number2;

    cout << number1 << " + " << number2 << " = " << number1 + number2 << "\n";
    cout << number1 << " * " << number2 << " = " << number1 * number2 << "\n";
    cout << number1 << " - " << number2 << " = " << number1 - number2 << "\n";

    return 0;
}